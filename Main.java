    //计算两个日期之间间隔天数
    public static int countDay(java.util.Date first,java.util.Date second){
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(first);
        int cnt = 0;
        while(calendar.getTime().compareTo(second) != 0){
            calendar.add(Calendar.DATE,1);
            cnt++;
        }
        return cnt;
    }
