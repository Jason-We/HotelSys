    private void SCconfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SCconfirmBtnActionPerformed
        // TODO add your handling code here:
        String name = SCnameTxt.getText().trim();
        String sexStr = null;
        if(SCsexMan.isSelected()){
            sexStr = "男";
        }else if(SCsexWoman.isSelected()){
            sexStr = "女";
        }
        String idCard = SCid.getText().trim();
        String hometown = SChometown.getText().trim();
        String roomStr = SCroomTxt.getText().trim();
        String moneyStr = SCmoneyTxt.getText().trim();
        //验证输入信息
        if(name.equals("")||sexStr.equals("")||idCard.equals("")||hometown.equals("")||roomStr.equals("")||moneyStr.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"填写信息必须完整!");
            return;
        }
        int room = -1;
        try{
            room  = Integer.parseInt(roomStr); //获取房间号
        }catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(this,"房间号必须位数字!");
            return;
        }
        float money = -1;
        try{
            money  = Float.parseFloat(moneyStr); 
        }catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(this,"押金必须位数字!");
            return;
        }
        //设置日期
        java.util.Date nDate = new java.util.Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String sDate = sdf.format(nDate);
        java.sql.Date now = java.sql.Date.valueOf(sDate);  //转型成java.sql.Date类型
        //Date time = new Date(new java.util.Date().getTime());
        //构造Customer对象
        Customer c = new Customer();
        c.setCard(idCard);
        c.setHometown(hometown);
        c.setName(name);
        c.setRoom(room);
        c.setMoney(money);
        c.setSex(sexStr);
        c.setTime(now);
        //判断可否登记
        ManagerFactory mf = ManagerFactory.newInstance();
        CustomerManager cm = mf.getCustomerManager();
        RoomManager rm = mf.getRoomManager();
        try {
            if(rm.getRoom(c.getRoom()) == null){
                javax.swing.JOptionPane.showMessageDialog(this,"房间不存在,请重新输入!");
                SCroomTxt.setText("");
            }else if (rm.getRoom(c.getRoom()).getState() == 1){
                javax.swing.JOptionPane.showMessageDialog(this,"房间已有人,请先查询空闲!");
                SCroomTxt.setText("");
            }else if(cm.addCustomer(c) && rm.setY(c.getRoom())){
                SignCustomer.setVisible(false);
                javax.swing.JOptionPane.showMessageDialog(this,"登记成功!");
                fresh.doClick();    //刷新主界面表格         
                //内容置空
                SCroomTxt.setText("");
                SCnameTxt.setText("");
                SCmoneyTxt.setText("");
                SCid.setText("");
                SChometown.setText("");
            }else{
                javax.swing.JOptionPane.showMessageDialog(this,"登记失败!");
                SignCustomer.setVisible(false);
                //内容置空
                SCroomTxt.setText("");
                SCnameTxt.setText("");
                SCmoneyTxt.setText("");
                SCid.setText("");
                SChometown.setText("");
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SCconfirmBtnActionPerformed
