    private void CRconfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CRconfirmBtnActionPerformed
        // TODO add your handling code here:
        String oldroom = oldRoom.getText().trim();
        String newroom = newRoom.getText().trim();
        int oldr,newr;
        if(oldroom.equals("")||newroom.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"房间号不能为空！");
            return;
        }else{
            try{
                oldr = Integer.parseInt(oldroom);
                newr = Integer.parseInt(newroom);
            }catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(this,"房间号必须为数字！");
                return;
            }
            try {
                Customer cold = hotel.ManagerFactory.newInstance().getCustomerManager().getCustomerByRoomId(oldr);
                Customer cnew = hotel.ManagerFactory.newInstance().getCustomerManager().getCustomerByRoomId(newr);
                if(cold == null){
                    javax.swing.JOptionPane.showMessageDialog(this,"原房间号不存在！");
                    return;
                }else if(cnew == null){
                    String oldGrade = hotel.ManagerFactory.newInstance().getRoomManager().getRoom(oldr).getGrade();
                    String newGrade = hotel.ManagerFactory.newInstance().getRoomManager().getRoom(newr).getGrade();
                    if(!oldGrade.equals(newGrade)){
                        javax.swing.JOptionPane.showMessageDialog(this,"级别不同的房间不允许调换，请先退房！");
                        return;
                    }
                    hotel.ManagerFactory.newInstance().getCustomerManager().change(oldr, newr);   //执行change（）方法
                    hotel.ManagerFactory.newInstance().getRoomManager().setN(oldr);     //更改原房间状态
                    hotel.ManagerFactory.newInstance().getRoomManager().setY(newr);     //更改新房间状态
                    javax.swing.JOptionPane.showMessageDialog(this,"调房成功！");
                    fresh.doClick();    //刷新主界面表格      
                    oldRoom.setText("");
                    newRoom.setText("");
                    ChangeRoom.setVisible(false);
                }else{
                    javax.swing.JOptionPane.showMessageDialog(this,"该房间已经有人！");
                    return;
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }           
        }             
    }//GEN-LAST:event_CRconfirmBtnActionPerformed
